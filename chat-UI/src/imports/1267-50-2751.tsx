// ==================== 动态卡片规范组件 ====================
// 规范：
// - 左侧：主标题 + 副标题
// - 右侧：点击按钮（可配置）
// - 支持左滑卡片忽略

interface SwipeableCardProps {
  title: string;
  subtitle: string;
  rightButton?: React.ReactNode;
  top: string;
  swipeX: number;
  setSwipeX: (x: number) => void;
  hasSwipedOut: boolean;
  setHasSwipedOut: (x: boolean) => void;
  isVisible: boolean;
  setIsVisible: (x: boolean) => void;
}

function SwipeableCard({
  title,
  subtitle,
  rightButton,
  top,
  swipeX,
  setSwipeX,
  hasSwipedOut,
  setHasSwipedOut,
  isVisible,
  setIsVisible
}: SwipeableCardProps) {
  const [isPressed, setIsPressed] = useState(false);
  const startXRef = useRef(0);

  if (!isVisible) return null;

  return (
    <div
      className="absolute bg-[#1b212c] content-stretch flex gap-[8px] items-center left-[14px] overflow-clip pl-[18px] pr-[8px] py-[16px] rounded-[12px] w-[364px]"
      style={{ top }}
      data-name="动态卡片"
    >
      {/* 忽略按钮 - 左滑渐显 */}
      <div
        className="absolute right-[8px] top-0 h-full flex items-center justify-center w-[80px] bg-[#ff4d4f] rounded-[12px]"
        style={{ opacity: hasSwipedOut ? 1 : Math.min(1, Math.max(0, -swipeX / 80)) }}
        onClick={() => setIsVisible(false)}
      >
        <span className="text-white text-[14px] font-medium">忽略</span>
      </div>

      {/* 卡片内容 */}
      <div
        className={`flex gap-[8px] items-center w-full cursor-pointer select-none ${isPressed && !hasSwipedOut ? 'scale-[0.98] opacity-80' : ''}`}
        style={{ transform: `translateX(${swipeX}px)`, transition: hasSwipedOut ? 'none' : (swipeX === 0 ? 'transform 0.2s ease-out' : 'none') }}
        onTouchStart={(e) => {
          if (hasSwipedOut) return;
          startXRef.current = e.touches[0].clientX;
          setIsPressed(true);
        }}
        onTouchMove={(e) => {
          if (hasSwipedOut) return;
          const diff = e.touches[0].clientX - startXRef.current;
          if (diff < 0) setSwipeX(diff);
        }}
        onTouchEnd={() => {
          if (hasSwipedOut) return;
          setIsPressed(false);
          if (swipeX < -80) {
            setSwipeX(-80);
            setHasSwipedOut(true);
          } else {
            setSwipeX(0);
          }
        }}
        onMouseDown={(e) => {
          if (hasSwipedOut) return;
          startXRef.current = e.clientX;
          setIsPressed(true);
        }}
        onMouseMove={(e) => {
          if (hasSwipedOut || startXRef.current === 0) return;
          const diff = e.clientX - startXRef.current;
          if (diff < 0) setSwipeX(diff);
        }}
        onMouseUp={() => {
          if (hasSwipedOut) return;
          setIsPressed(false);
          startXRef.current = 0;
          if (swipeX < -80) {
            setSwipeX(-80);
            setHasSwipedOut(true);
          } else {
            setSwipeX(0);
          }
        }}
        onMouseLeave={() => {
          if (hasSwipedOut) return;
          if (isPressed) {
            setIsPressed(false);
            startXRef.current = 0;
            if (swipeX < -80) {
              setSwipeX(-80);
              setHasSwipedOut(true);
            } else {
              setSwipeX(0);
            }
          }
        }}
      >
        {/* 左侧：主标题 + 副标题 */}
        <div className="content-stretch flex flex-[1_0_0] flex-col gap-[4px] items-start min-h-px min-w-px not-italic overflow-clip relative">
          <div className="flex flex-col font-['MiSans:Medium',sans-serif] justify-center leading-[0] relative shrink-0 text-[16px] text-[rgba(255,255,255,0.9)] w-full">
            <p className="leading-[normal]">{title}</p>
          </div>
          <p className="font-['MiSans:Regular',sans-serif] leading-[normal] relative shrink-0 text-[14px] text-[rgba(255,255,255,0.5)] w-full">{subtitle}</p>
        </div>

        {/* 右侧：点击按钮 */}
        {rightButton && (
          <div className="relative shrink-0 size-[26px]">
            {rightButton}
          </div>
        )}
      </div>
    </div>
  );
}

